package world;

public class Palencia{
	
	int px;
	int py;
	
	public Palencia(int posx, int posy){
		px = posx;
		py = posy;
	}

	public int getPx() {
		return px;
	}

	public void setPx(int px) {
		this.px = px;
	}

	public int getPy() {
		return py;
	}

	public void setPy(int py) {
		this.py = py;
	}
	
	
}